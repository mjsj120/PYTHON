# % : 나머지 구하기
# // : 몫 구하기  
# == : 같다 
# != :  같지 않다
# not (): 같지 않다


print(1 + 1) #2
print(3 - 2) #1
print(5 * 2) #10
print(6 / 3) #2

print(2 ** 3) #2^3 = 8
print(5 % 3) #2    # % : 나머지 구하기
print(5 // 3) #1   # // : 몫 구하기  

print(10 > 4) #True
print(4 >= 7) #False

print(3 == 3) #True  # == : 같다 
print(3 + 4 ==7) #True

print(1 != 3) #True # != :  같지 않다
print(not (1 != 3)) #False

print((3 > 0) and (3 < 5)) #True
print((3 > 0) & (3 < 5)) #True

print((3 > 0) or (3 < 5)) #True
print((3 > 0) | (3 < 5)) #True

print(5 > 4 > 3) #True
print(5 > 4 > 7) #False